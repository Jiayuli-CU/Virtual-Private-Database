CREATE TABLE ADMINISTRATION_CONFERENCE
(
  ID VARCHAR2(20) NOT NULL,
  BEGIN_TIME TIMESTAMP,
  END_TIME TIMESTAMP,
  CONFERENCE_ROOM VARCHAR2(20),
  REASON VARCHAR2(40),
  APPLY_PERSON VARCHAR2(20) NOT NULL,
  BASE VARCHAR2(20),
  CONSTRAINT  ADMINISTRATION_CONFERENCE_PK PRIMARY KEY (ID) 
  ENABLE 
